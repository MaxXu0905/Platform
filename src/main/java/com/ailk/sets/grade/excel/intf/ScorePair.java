package com.ailk.sets.grade.excel.intf;

public class ScorePair {

	private double scores;
	private int count;

	public double getScores() {
		return scores;
	}

	public void setScores(double scores) {
		this.scores = scores;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
